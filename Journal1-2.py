def main():
    #Print sum of two floating point numbers
    float_sum = 2.5 + 7
    print(float_sum)
    print(type(float_sum))
    
    #Print difference between two integers
    int_diff = 6-9
    print(int_diff)
    print(type(int_diff))
    
    #Print product of a floating point number and an integer
    product = 3.7*6
    print(product)
    print(type(product))
    
#run program
if __name__=="__main__":
    main()